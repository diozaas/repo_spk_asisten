    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //Radio Button Status Bekerja
        if (rb_bekerja.isSelected()){
            s = rb_bekerja.getText();
        }
        else if (rb_tidakbekerja.isSelected()){
            s = rb_tidakbekerja.getText();
        }

        //Memilih bidang yang dikuasai
        if(chk_jaringan.isSelected()){
            b1 = chk_jaringan.getText() ;
        }
        if (chk_pemrograman.isSelected()){
            b2 = chk_pemrograman.getText();
        }
        if (chk_troubleshooting.isSelected()){
            b3 = chk_troubleshooting.getText();
        }
        if(chk_desain.isSelected()){
            b4 = chk_desain.getText();
        }

        //mengubah value combo box semester dari String ke Integer
        sms = cmb_sms.getSelectedItem().toString();
        smst = Integer.valueOf(sms);

        //mengubah data pada tabel datadiri
        try{
            int row = tabeldatadiri.getSelectedRow();

            if (row != -1){
                asisten asistenUpdate = new asisten();

                asistenUpdate.setNpm(txt_npm.getText());
                asistenUpdate.setNama(txt_nama.getText());
                if(rb_l.isSelected()){
                    asistenUpdate.setJk(rb_l.getText());
                }
                else if(rb_p.isSelected()){
                    asistenUpdate.setJk(rb_p.getText());
                }
                asistenUpdate.setJurusan(cmb_jurusan.getSelectedItem().toString());
                asistenUpdate.setSms(cmb_sms.getSelectedItem().toString());
                asistenUpdate.setKls(cmb_kelas.getSelectedItem().toString());
                asistenUpdate.setIpk(txt_ipk.getText());
                asistenUpdate.setNohp(txt_hp.getText());
                if(rb_bekerja.isSelected()){
                    asistenUpdate.setSbk(rb_bekerja.getText());
                }
                else if(rb_tidakbekerja.isSelected()){
                    asistenUpdate.setSbk(rb_tidakbekerja.getText());
                }
                asistenUpdate.setAlamat(txt_alamat.getText());
                asistenUpdate.setBidang_1(chk_desain.getText());
                asistenUpdate.setBidang_2(chk_pemrograman.getText());
                asistenUpdate.setBidang_3(chk_troubleshooting.getText());
                asistenUpdate.setBidang_4(chk_desain.getText());

                sqlUpdate = "UPDATE DATADIRI SET NAMA = ?, JK = ?, JURUSAN = ?, SEMESTER = ?, KELAS = ?,"
                + " IPK = ?, NOHP = ?, STATUS = ?, ALAMAT = ?, BIDANG_1 = ?, BIDANG_2 = ?, BIDANG_3 = ?, BIDANG_4 = ? WHERE NPM = ?";

                PreparedStatement preparedStatement = koneksidb.getConnection().prepareStatement(sqlUpdate);

                preparedStatement.setString(1, asistenUpdate.getNama());
                preparedStatement.setString(2, asistenUpdate.getJk());
                preparedStatement.setString(3, asistenUpdate.getJurusan());
                preparedStatement.setString(4, asistenUpdate.getSms());
                preparedStatement.setString(5, asistenUpdate.getKls());
                preparedStatement.setString(6, asistenUpdate.getIpk());
                preparedStatement.setString(7, asistenUpdate.getNohp());
                preparedStatement.setString(8, asistenUpdate.getSbk());
                preparedStatement.setString(9, asistenUpdate.getAlamat());
                preparedStatement.setString(10, asistenUpdate.getBidang_1());
                preparedStatement.setString(11, asistenUpdate.getBidang_2());
                preparedStatement.setString(12, asistenUpdate.getBidang_3());
                preparedStatement.setString(13, asistenUpdate.getBidang_4());
                preparedStatement.setString(14, asistenUpdate.getNpm());

                int isSuccess = preparedStatement.executeUpdate();
                if (isSuccess != 0){
                    tampildataketabeldatadiri();
                    JOptionPane.showMessageDialog(null, "Data berhasil diubah");
                    reset();
                }
                else if (isSuccess == 0){
                    JOptionPane.showMessageDialog(null, "Data gagal diubah");
                }
            }
        }catch(SQLException | HeadlessException exception){
            JOptionPane.showMessageDialog(this, "Data tidak berhasil diubah"+exception);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

